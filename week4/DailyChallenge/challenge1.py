# Exercise 1: Quizz

# 1. CLASS Definition: A blueprint for creating objects. It defines
# attributes and methods.


# 2. INSTANCE
# Definition: An individual object created from a class.

# 3. ENCAPSULATION Definition: Restricting direct access to data to prevent
# accidental modification.


# 4. ABSTRACTION Definition: Hiding complex implementation and enforcing a
# template for subclasses.

# 5. INHERITANCE
# Definition: A class deriving attributes and methods from another class.


# 6. MULTIPLE INHERITANCE
# Definition: A class inheriting from more than one parent class.


# 7. POLYMORPHISM Definition: Different classes using the same method name
# for different behaviors.


# 8. METHOD RESOLUTION ORDER (MRO)
# Definition: The order in which Python searches for methods in a hierarchy.
